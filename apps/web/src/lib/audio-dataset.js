export function getAudioDatasetEntryById(entryId) {
  return audioDataset.find((item) => String(item.id) === String(entryId)) ?? null;
}

export function searchAudioDataset(query) {
  const normalized = (query || "").trim().toLowerCase();
  if (!normalized) return audioDataset;
  return audioDataset.filter((item) =>
    [item.mandarinText, item.sampleKey, item.pronunciation, item.fileName]
      .join(" ")
      .toLowerCase()
      .includes(normalized),
  );
}

